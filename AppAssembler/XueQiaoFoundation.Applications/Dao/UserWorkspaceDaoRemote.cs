        }
       
        public WorkspaceWindowTree GetUserWorkspaceData(LandingInfo landingInfo, out int? dataVersion)
        {
            dataVersion = null;

            var resp = XqThriftLibConfigurationManager.SharedInstance.TradeHostingTerminalAoHttpStub.getUserSetting(landingInfo, UserWorkspaceStoreKey);
            if (resp == null || resp.SourceException != null || resp.CorrectResult == null)
            {
                AppLog.Error("Failed to GetUserWorkspaceData.");
                return null;
            }

            var content = resp.CorrectResult.Content;
            dataVersion = resp.CorrectResult.Version;

            if (content == null) return null;
            try
            {
                var deserializedData = JsonConvert.DeserializeObject<WorkspaceWindowTree>(content);
                if (deserializedData == null)
                {
                    AppLog.Warn($"Json deserialize failed for output:{content}");
                }
                return deserializedData;
            }
            catch (Exception e)
            {
                AppLog.Error($"Deserialize workspaceData failed. e:{e}");
                return null;
            }
        }
        
        public int? SaveUserWorkspaceData(LandingInfo landingInfo, WorkspaceWindowTree userWorkspaceDataTree, int dataVersion)
        {
            if (userWorkspaceDataTree == null) return null;
            if (landingInfo == null) return null;

            string serializeContent = null;
            try
            {
                serializeContent = JsonConvert.SerializeObject(userWorkspaceDataTree);
            }
            catch (Exception e)
            {
                AppLog.Error($"Serialize userWorkspace Data failed. e:{e}");
                return null;
            }
            if (serializeContent == null)
            {
                AppLog.Error($"Failed to serialize userWorkspaceDataTree.");
                return null;
            }
            
            var updateSetting = new HostingUserSetting
            {
                Content = serializeContent,
                Version = dataVersion
            };

            var saveResp = XqThriftLibConfigurationManager.SharedInstance.TradeHostingTerminalAoHttpStub.updateUserSetting(landingInfo, UserWorkspaceStoreKey, updateSetting);
            if (saveResp.SourceException == null) return updateSetting.Version;

            if (saveResp.CustomParsedExceptionResult?.BusinessErrorCode == ErrorCodeConstants.ERROR_CONFIG_VERSION_LOW
                || saveResp.CustomParsedExceptionResult?.BusinessErrorCode == ErrorCodeConstants.ERROR_CONFIG_LOST)
            {
                var dataVersionResp = XqThriftLibConfigurationManager.SharedInstance.TradeHostingTerminalAoHttpStub.getUserSettingVersion(landingInfo, UserWorkspaceStoreKey);

                bool needRetry = false;
                if (dataVersionResp.CustomParsedExceptionResult?.BusinessErrorCode == ErrorCodeConstants.ERROR_CONFIG_LOST)
                {
                    needRetry = true;
                }
                else if (dataVersionResp.CorrectResult is int queriedVersion)
                {
                    updateSetting.Version = queriedVersion + 1;
                    needRetry = true;
                }

                if (needRetry)
                {
                    // retry again
                    saveResp = XqThriftLibConfigurationManager.SharedInstance.TradeHostingTerminalAoHttpStub.updateUserSetting(landingInfo, UserWorkspaceStoreKey, updateSetting);
                    if (saveResp.SourceException == null)
                        return updateSetting.Version;
                    else
                    {
                        AppLog.Error($"Failed to SaveLoginUserWorkspaceData.");
                        return null;
                    }
                }
                return null;
            }
            else
            {
                return null;
            }
        }
    }
